// func processChebyshev4AVX2Asm(input, output, gains *float32, count int)
TEXT ·processChebyshev4AVX2Asm(SB), NOSPLIT, $0-32
	MOVQ input+0(FP), SI
	MOVQ output+8(FP), DI
	MOVQ gains+16(FP), AX
	MOVQ count+24(FP), CX

	VBROADCASTSS 0(AX), Y8
	VBROADCASTSS 4(AX), Y9
	VBROADCASTSS 8(AX), Y10
	VBROADCASTSS 12(AX), Y11

	MOVL $0x3f800000, AX
	VMOVD AX, X12
	VBROADCASTSS X12, Y12
	MOVL $0xbf800000, AX
	VMOVD AX, X13
	VBROADCASTSS X13, Y13
	MOVL $0x40000000, AX
	VMOVD AX, X14
	VBROADCASTSS X14, Y14

	TESTQ CX, CX
	JLE done

loop:
	VMOVUPS (SI), Y0
	VMAXPS Y13, Y0, Y0
	VMINPS Y12, Y0, Y0

	VMOVAPS Y12, Y1
	VMOVAPS Y0, Y2
	VMULPS Y8, Y2, Y6

	VMULPS Y0, Y2, Y3
	VMULPS Y14, Y3, Y3
	VSUBPS Y1, Y3, Y3
	VMULPS Y9, Y3, Y4
	VADDPS Y4, Y6, Y6
	VMOVAPS Y2, Y1
	VMOVAPS Y3, Y2

	VMULPS Y0, Y2, Y3
	VMULPS Y14, Y3, Y3
	VSUBPS Y1, Y3, Y3
	VMULPS Y10, Y3, Y4
	VADDPS Y4, Y6, Y6
	VMOVAPS Y2, Y1
	VMOVAPS Y3, Y2

	VMULPS Y0, Y2, Y3
	VMULPS Y14, Y3, Y3
	VSUBPS Y1, Y3, Y3
	VMULPS Y11, Y3, Y4
	VADDPS Y4, Y6, Y6

	VMOVUPS Y6, (DI)
	ADDQ $32, SI
	ADDQ $32, DI
	SUBQ $8, CX
	JG loop

done:
	VZEROUPPER
	RET
